"""If Only"""

a = 10

if a == 10:

    print("The value of a is 10")



"""If Else"""


a = 10

if a == 10:

    print("The value of a is 10")

else:

    print("Value of a is invalid")



"""If Elif Else"""  #  We can have multiple elif statement based on the condition we want to check



a = 10

if a == 10:

    print("The value of a is 10")

elif a == 9:

    print("The value of a is 9")

elif a == 8:

    print("The value of a is 8")

else:

    print("Value of a is invalid")




