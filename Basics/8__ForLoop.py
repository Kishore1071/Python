"""we can for loop string, list, tuple and range"""

#  String
pet = "dog"

for a in pet:

    print(a)


#  List

numbers = [1, 2, 3, 4]

for n in numbers:

    print(n)


#  Tuple

animals = ['Lion', 'Tiger', 'Elephant']

for animal in animals:

    print(animal)


#  Range

for number in range(0, 11):

    print(number)